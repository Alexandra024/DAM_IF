public class EjNum {
    public static void multiplosDe5(String[] args) {
        // Imprimir los múltiplos de 5 entre 0 y 500
        for (int i = 0; i <= 500; i += 5) {
            System.out.print(i + " ");
        }
    }

    public static void primeros50NumerosPares(String[] args) {
        // Imprimir los primeros 50 números pares
        for (int i = 0; i < 50; i++) {
            System.out.print(i * 2 + " ");
        }
    }
    
    
}
